package ru.geekbrains.homework01.regular;

/**
 * Другой, очень полезный класс приложения. Здесь мы можем описать
 * его основное назначение и способы взаимодействия с ним.
 */
public class OtherClass {

    /**
     * Метод для сложения двух целых чисел
     *
     * @param a первое слагаемое
     * @param b второе слагаемое
     * @return сумма a и b.
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * Метод для деления двух целых чисел
     *
     * @param a делимое
     * @param b делитель
     * @return частное a и b.
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * Метод для умножения двух целых чисел
     *
     * @param a первый множитель
     * @param b второй множитель
     * @return произведение a и b.
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * Метод для вычисления разности двух целых чисел
     * @param a первое число
     * @param b второе число
     * @return результат
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
